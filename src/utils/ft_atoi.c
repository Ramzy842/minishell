#include "../../minishell.h"

int ft_atoi(char *str)
{
int i = 0;
int num = 0;

    while(str[i] == ' ')
        i++;
    while(str[i]>='0'&& str[i]<='9')
        {
            num  = num*10+str[i]-'0';
                i++;
        }
        return num;
}       
